#include "libft.h"

int		ft_atoi(const char *str)
{
	int x;
	int num;

	num = 1;
	x = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == 32 || *str == 0)
	{
		str++;
	}
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			num = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		x = x * 10 + (*str - 48);
		str++;
	}
	return (x * num);
}
