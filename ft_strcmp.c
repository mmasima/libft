#include "libft.h"

int	ft_strcmp(char const *s1, char const *s2)
{
	int x;

	x = 0;
	while (s2[x] != '\0' && s1[x] != '\0' && s2[x] == s1[x])
	{
		x++;
	}
	return (s1[x] - s2[x]);
}
